; draws a char fron the font
; ax = asciii CLOBBERS aX
; bl = x (40), bh = y (48)
draw_char:

	push cx
	push dx
	push ds
	push di
	push es
	push ax
	push bx
	

	
	mov dx, codeSeg
	mov ds, dx
	mov dx, fbSeg
	mov es, dx
	
	mov cx, 8
	
	xor di,di
	
	push bx
	and bx, 0xff

	add di, bx
	pop bx
	
	mov bl,bh
	xor bh,bh
;	shr bx, 1
;	shr bx, 1
;	shr bx, 1
;	shr bx, 1
;	shr bx, 1
;	shr bx, 1
;	shr bx, 1
;	shr bx,1

	
	push ax
	mov ax, bx
	mov bx, 800
	mul bx
	add di, ax
	pop ax
	
	mov bx, font
	push ax
	and ax, 0x7
	add bx,ax 
	pop ax
	and ax, 0xfff8
	shl ax, 1
	shl ax,1
	shl ax,1
	add bx, ax

.drawloop:
	mov dl, [bx]
	not dl
	
	mov [es:di], dl
	
    	add di, 40 ; line
    	mov [es:di], dl
	add bx, 8
	add di, 40

	loop .drawloop

	pop bx
	pop ax
	pop es
	pop di
	pop ds
	pop dx
	pop cx
	ret
