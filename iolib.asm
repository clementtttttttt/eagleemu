iolib_setup:
	
	mov bp, sp
	mov bx, [bp] ;get return value

	mov ax, stackseg
	mov ss, ax
	mov sp, stack
	

	push bx ;push it
	
	mov bx, 0xc000
	mov es, bx
	mov byte es:[0], 0
mov byte es:[1],SD_CS|SD_CLK|SD_TX
	mov byte es:[6], 0
	mov byte es:[7],0xff
	.uart_init:
	mov byte es:[8+3], 0x80;dlab
	mov byte es:[8+4], 2 ; enable all 
	mov byte es:[8], 1;115200
	mov byte es:[9], 0;115200
	mov byte es:[8+3], 0b0000111 ;8 bit, two stop bits
	mov byte es:[8+1], 1b; yes ints
	;mute all channels and reset
	mov byte es:[3], 0x1c
	mov byte es:[2], 0b11
	nop
	mov byte es:[2], 0x0
	mov byte es:[3], 0x19
	mov byte es:[2], 0
	mov byte es:[3], 0x18
	mov byte es:[2], 0
	


	xor bx,bx
	mov es,bx
	mov word [es:0], vsync
	mov word [es:2], codeSeg
	mov word [es:4*1], serint
	mov word [es:4*1+2], codeSeg
	mov word [es:4*3],keyint
	mov word [es:4*3+2],codeSeg
		mov bx, 0xa000
	mov es, bx
	xor al, al
	xor di, di
	mov cx, 320/8*500
	rep stosb

	mov bx, datSeg
	mov es, bx
	mov ds, bx
	mov byte es:[currY],0
	mov byte es:[currX],0
	mov word es:[shift], 0
	
	
	
	call clear_screen
	


	ret
	

keyint:
