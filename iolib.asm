sd_write:
	push cx
	push es
	push bx
	
	mov cx, 0xc000
	mov es, cx ; MMIO segment
	
	mov cx, 8
	

	.send_loop:
	and bl, (~SD_TX)&(~SD_CS)

	;es : 1 = 0xc0001, PCR reg with sd controls
	test al, 0x80
	jz .zero
	
	or BYTE bl, SD_TX
		
	
	.zero:
	
	test BYTE [spi_flags], 0xff
	jz .cs_zero

	or BYTE bl, SD_CS
	.cs_zero:

	;tick sd clk

	and BYTE bl, ~SD_CLK ;mode 3
	mov BYTE [es:1], bl
	or bl, SD_RXCLK
	mov BYTE [es:1],bl
	and bl, ~SD_RXCLK
	mov BYTE [es:1],bl
	or BYTE bl, SD_CLK
	mov BYTE [es:1], bl

	rol al, 1
	
	loop .send_loop
	
	pop bx
	pop es
	pop cx
	ret
	



