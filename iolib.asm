putc:
	
	
%IFDEF USE_SERIAL
        push ds
        push bx


		mov bx, 0xc000
        mov ds, bx
       	mov [8], al

        mov bx, 5+8
        .wait_empty:
        test byte [bx], 1<<5
        jz .wait_empty
        .end:
        
        pop bx
        pop ds

        ret

%ELSE
	push bx
	push ds
	

	mov bx, datSeg
	mov ds, bx
	mov bx,[currX]
	 
	xor ah, ah	
	cmp al, 8
	jz .skip_reset_x
	
	cmp al, 27
	jz .skip_reset_x
	
	cmp bh, 23

	jbe .skip_scroll
		
	
.scroll:
	
	push di
	push es
	push ds
	
	
	mov cx, 0xa000
	mov ds, cx
	mov es, cx
	
	mov cx, (320/8*480/2) ;minus the row
	mov si, 320/8*20 ; one row ahead
	xor di, di
		
	cld
	rep movsw 
	
	pop ds
	pop es
	pop di
	
	
	dec bh
	;dec bh
	;mov bl,1
	xor bl,bl
	;jmp .skipprn	
	.skip_scroll:


	cmp al, 10
	jz .yes_reset_x

	call draw_char
	
	inc bl
	
	cmp bl, 320/8
	jb .skip_reset_x
	
	
	.yes_reset_x:
	xor bl, bl
	
	;cmp bh,23
	;jg .skip_reset_x
	inc BH

	.skip_reset_x:
	cmp al, 8

	jnz .skip_dec_X
	dec bl
	.skip_dec_X:

	
	mov [currX], bx
	
	
	pop ds
	pop bx	
	
	ret


