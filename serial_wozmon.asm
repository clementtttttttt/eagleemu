;al = read value
sd_read_fe:
        push cx
        push es
        push bx

        mov cx, 0xc000
        mov es, cx

	
        .wait_for_zero_bit:
        or bl, SD_TX ; send ffz
        or bl, SD_CLK
        mov [es:1], bl
        and bl, ~SD_CLK
        mov [es:1], bl

        or bl, SD_RXCLK ;tick receiving shiftreg
        mov [es:1], bl
        and bl, ~SD_RXCLK
        mov [es:1], bl

        mov al, [es:1]
        cmp  al, 0xfe ;read from pcr address returns sdcard receiving shiftreg

        jnz .wait_for_zero_bit ;no zero.

        
        ;return value in al
        

        pop bx
        pop es
        pop cx
        ret


;al = read value
sd_read:
	push cx
	push es
	push bx

	mov cx, 0xc000
	mov es, cx

	
	.wait_for_zero_bit:
	or bl, SD_TX ; send ffz
	or bl, SD_CLK
	mov [es:1], bl
	and bl, ~SD_CLK
	mov [es:1], bl
	
	or bl, SD_RXCLK ;tick receiving shiftreg
        mov [es:1], bl
        and bl, ~SD_RXCLK
        mov [es:1], bl

	mov al, [es:1]
	test  al, 1 ;read from pcr address returns sdcard receiving shiftreg
	
	jnz .wait_for_zero_bit ;no zero.

	mov cx, 7 

	.receive_remaining:
	mov byte [es:1], SD_CLK | SD_TX ;tick sdcard
	mov byte [es:1], SD_TX

	mov byte [es:1], SD_RXCLK | SD_TX ; tick receiving shiftreg
	mov byte [es:1], SD_TX
	
	loop .receive_remaining
	
	;return value in al 
	mov al, [es:1]

	pop bx
	pop es
	pop cx
	ret 

sd_read_nopoll:
        push cx
        push es
        push bx

        mov cx, 0xc000
        mov es, cx

        
        mov cx, 8

        .receive_remaining:
        mov byte [es:1], SD_CLK | SD_TX ;tick sdcard
        mov byte [es:1], SD_TX

        mov byte [es:1], SD_RXCLK | SD_TX ; tick receiving shiftreg
        mov byte [es:1], SD_TX

        loop .receive_remaining

        ;return value in al 
        mov al, [es:1]

        pop bx
        pop es
        pop cx
        ret



;al stores value to be sent, will be trashed
sd_write:
	push cx
	push es
	push bx
	
	mov cx, 0xc000
	mov es, cx ; MMIO segment
	
	mov cx, 8
	

	.send_loop:
	xor bl,bl

	;es : 1 = 0xc0001, PCR reg with sd controls
	test al, 0x80
	jz .zero
	
	or BYTE bl, SD_TX
		
	
	.zero:
	
	test BYTE [spi_flags], 1
	jz .cs_zero

	or BYTE bl, SD_CS
	.cs_zero:

	;tick sd clk

	and BYTE bl, ~SD_CLK ;mode 3
	mov BYTE [es:1], bl
	or BYTE bl, SD_CLK
	mov BYTE [es:1], bl

	rcl al, 1
	
	loop .send_loop
	
	pop bx
	pop es
	pop cx
	ret
	


